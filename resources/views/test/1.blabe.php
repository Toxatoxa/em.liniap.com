                                                <t217/>
                                                Ajatus Smart in the dark -tuotteiden kehittämisestä syntyi perustajamme
                                                Ann-Sofie Hermanssonin huomatessa, että hyvinpukeutuville miehille ja
                                                naisille ei ole olemassa lainkaan hyvännäköisiä heijastimia. Kaikki
                                                alkoi siis ongelmasta. Perustajamme Ann-Sofie oli vastikään ostanut
                                                itselleen lämpimän ja kauniin mustan takin, josta tuli heti kättelyssä
                                                hänen lempivaatteensa. Niinpä tuntuikin kovin ikävältä pilata takkia
                                                keltaisilla heijastimilla tai lapsellisilla aiheilla, kun hän lähti
                                                iltakävelylle, kaupunkiin töihin tai ystäviä tapaamaan. Hänen oli pakko
                                                valita joko tyylikkyys tai turvassa oleminen.
                                                <t218/>
                                                <t219/>
                                                Hän alkoi etsiä markkinoilta aikuisille tarkoitettuja heijastimia
                                                löytämättä kuitenkaan yhtään. Hän huomasi myös todella harvojen
                                                hyvinpukeutuvien ihmisten käyttävän heijastimia. Kysymyksessä oli
                                                yksinkertaisesti markkinarako, josta tuli sittemmin koko yrityksen
                                                perusta. Heti alusta asti pyrkimyksenä oli tarjota muotoiltuja,
                                                tyylikkäitä ja turvallisia heijastimia, joita ihmiset haluaisivat
                                                käyttää! Ann-Sofie alkoi etsiä mahdollisuuksia toteuttaa ajatustaan,
                                                joka tuohon aikaan liittyi heijastintuotteisiin.
                                                <t220/>
