<t235/>[/vc_column_text][/vc_tta_section][vc_tta_section title="Periaatteemme" tab_id="1459176809496-c76a92c8-ef0b"][vc_column_text]Smart in the dark haluaa ja pyrkii luomaan innovatiivisia tuotteita, joita kanssaihmisemme käyttäisivät ilolla ja ylpeydellä. Kehittämissämme ja tarjoamissamme tuotteissa yhdistyvät muotoilu, turvallisuus, laatu ja toimivuus. Mielestämme on olennaista tarjota tuotteita, jotka luovat todellista turvallisuutta ja saavat aikaan muutoksen. Smart in the dark -tuotteen käyttäminen voi saada aikaan arvokkaan muutoksen.
<t236/>
<t237/>Kolme työskentelyperiaatettamme ovat muotoilu, turvallisuus ja harkinta.
<t238/>Haluamme,
että ihmiset näkyisivät vapaasti ja turvallisesti liikenteessä riippumatta siitä, ovatko he suurkaupungissa, maaseudulla, matkalla töihin, vapaa-ajan viettoon tai juhliin. Tuotteemme perustuvat suurelta osin harkintaan, huomioimme niiden käyttäjät sekä heidän läheisensä.
<t239/>
<t240/>Periaatteenamme on myös huolehtia heistä, jotka työskentelevät kanssamme asiakkaana, valmistajana tai yhteistyökumppanina.
<t241/>Smart in the dark® -tuotteiden pääasiallinen tavoite on huolehtia ihmisistä liikenteessä. Intohimonamme on saada ihmiset näkymään liikenteessä turvallisemmin.[/vc_column_text][/vc_tta_section][vc_tta_section title="Muotoilumme" tab_id="1459177316302-88802b76-fe84"][vc_column_text css_animation="bottom-to-top"]Smart in the dark suunnittelee erittäin turvallisia tuotteita. Haluamme niiden tuovan todellista turvallisuutta, toisin sanoen muotoilemme niitä viimeisen päälle tekemättä sitä kuitenkaan turvallisuuden kustannuksella. Smart in the darkin ensimmäinen mallisto on saanut innoituksensa Pohjois-Ruotsista. Haluamme suunnitella heijastimia, jotka luovat tunnelmaa ja joita voi käyttää ylpeydellä. Haluamme, että ne koetaan enemmän kädessä olevana koristeena, kauniina päällysvaatteen varusteena kuin turvallisuustuotteena.
<t242/>
<t243/>Ensimmäinen kokoelmamme sai innoituksensa pohjoisen luonnosta ja kulttuurista. Tämä on Nordic Silver Collection -kokoelman tarina:
<t244/>Ennenvanhaan pohjoisen asukkaat muuttivat paikasta toiseen. Silloin oli tärkeää valita mukaan arvokkaita, kestäviä ja keveitä matkavarusteita. Heidän elinikäinen rakkautensa oli hopea ja heitä kutsuttiinkin loistavaksi kansaksi, koska he näkyivät jo kaukaa. Haluamme levittää pohjoisen hopeaa laajalle ja näin valaista muuten maailman pimeämmän kolkan. Yksinkertainen tapa saada aikaan arvokas muutos.
<t245/>
