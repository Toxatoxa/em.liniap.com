                                                löytäminen ovat myös omia haasteitaan. Ensimmäinen Nordic Silver
                                                Collection -mallisto esiteltiin lokakuussa vuonna 2014 Göteborgin
                                                yrittäjä- ja johtajuuspäivillä. Näin alkoi vihdoinkin yrityksemme
                                                kehityksen seuraava vaihe, jossa työskentelemme intohimoisesti
                                                saadaksemme ihmiset huolehtimaan itsestään ja läheisistään liikenteessä.
